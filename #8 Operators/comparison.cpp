#include <iostream>
using namespace std;

int main(){
    //Operator Perbandingan
    //Operator Perbandingan digunakan untuk membandingkan dua nilai (atau variabel). Hal ini penting dalam pemrograman, karena membantu kita menemukan jawaban dan membuat keputusan. Nilai yang dikembalikan dari suatu perbandingan adalah 1 atau 0, yang berarti benar atau salah. Nilai-nilai ini dikenal sebagai nilai Boolean. Dalam contoh berikut, kita menggunakan operator lebih besar dari (>) untuk mencari tahu apakah 5 lebih besar dari 3:
    int x = 5;
    int y = 3;
    cout << (x > y) << endl;
    cout << "======================" << endl;

    cout << (x == y) << endl;

    //Daftar semua operator perbandingan:
    //x == y
    //x != y
    //x > y
    //x < y
    //x >= y
    //x <= y
}