#include <iostream>
using namespace std;

int main(){
    //Bahasa Boolean C++
    //Sangat sering, dalam pemrograman, Anda memerlukan tipe data yang hanya dapat memiliki satu dari dua nilai, seperti:
    //Ya/Tidak
    //Untuk ini, C++ memiliki bool tipe data, yang dapat mengambil nilai true (1) atau false(0).

    //Nilai Boolean
    //Variabel boolean dideklarasikan dengan bool kata kunci dan dapat mengambil nilai true atau false:
    bool isCodingFun = true;
    bool isFishTasty = false;
    cout << isCodingFun << endl;
    cout << isFishTasty << endl;
    cout << "======================" << endl;

    //Dari contoh diatas, Anda dapat membaca bahwa suatu true nilai mengambilkan 1, dan false mengambilkan 0.
}