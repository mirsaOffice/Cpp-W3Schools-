#include <iostream>
#include <string>
using namespace std;

int main(){
    //Menambahkan Angka Dan String
    //Jika Anda menambahkan dua angka, hasilnya akan menjadi angka:
    int x = 10;
    int y = 20;
    int z = x + y;
    cout << z << endl;
    cout << "======================" << endl;

    //Jika Anda menambahkan dua string, hasilnya akan berupa penggabungan string:
    string a = "10";
    string b = "20";
    string c = a + b;
    cout << c << endl;
    cout << "======================" << endl;

    //Jika Anda menambahkan angka ke dalam string, akan terjadi kesalahan
}